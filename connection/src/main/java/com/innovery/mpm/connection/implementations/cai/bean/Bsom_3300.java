package com.innovery.mpm.connection.implementations.cai.bean;

import com.innovery.mpm.connection.interfaces.cai.bean.Bsom_3300Interface;

public class Bsom_3300 implements Bsom_3300Interface {

	private String ip;
	private final int port = 3300;
	private String user;
	private String password;
	
	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public int getPort() {
		return port;
	}
}
