package com.innovery.mpm.allresource.implementations.bean.air;

import java.util.ArrayList;
import java.util.List;

import com.innovery.mpm.allresource.interfaces.bean.air.BalanceAndDateInterface;
import com.innovery.mpm.allresource.interfaces.bean.air.DedicatedAccountInterface;

public class BalanceAndDate implements BalanceAndDateInterface{

	private String main_balance;
	private List<DedicatedAccountInterface> dedicated_accounts;
	
	public BalanceAndDate(){
		main_balance = "";
		dedicated_accounts = new ArrayList<DedicatedAccountInterface>();
	}

	public String getMain_balance() {
		return main_balance;
	}

	public void setMain_balance(String main_balance) {
		this.main_balance = main_balance;
	}

	public List<DedicatedAccountInterface> getDedicated_accounts() {
		return dedicated_accounts;
	}

	public void setDedicated_accounts(List<DedicatedAccountInterface> dedicated_accounts) {
		this.dedicated_accounts = dedicated_accounts;
		
	}
}
