package com.innovery.mpm.allresource.implementations.bean.air;

import com.innovery.mpm.allresource.interfaces.bean.air.FafInterface;

public class Faf implements FafInterface{

	private String msisdn;
	private String owner;
	private String number;
	private String k_indicator;
	
	public Faf(){
		this.msisdn=null;
		this.owner=null;
		this.number=null;
		this.k_indicator=null;
	}
	
	public Faf(String msisdn){
		this.msisdn=msisdn;
		this.owner=null;
		this.number=null;
		this.k_indicator=null;
	}
		
	public String getMSISDN(){
		return this.msisdn;
	}
	
	public String getOwner(){
		return this.owner;
	}
	
	public String getNumber(){
		return this.number;
	}
	
	public String getKindicator(){
		return this.k_indicator;
	}
	
	public void setMSISDN(String msisdn){
		this.msisdn=msisdn;
	}
	
	public void setOwner(String owner){
		this.owner=owner;
	}
	
	public void setNumber(String number){
		this.number=number;
	}
	
	public void setKindicator(String k_indicator){
		this.k_indicator=k_indicator;
	}
}
