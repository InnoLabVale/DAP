package com.innovery.mpm.allresource.implementations.bean.air;

import com.innovery.mpm.allresource.interfaces.bean.air.AccumulatorInterface;

public class Accumulator implements AccumulatorInterface{
	
	private String msisdn;
	private String acc_id;
	private String acc_value_abs;
	private String acc_value_rel;
	private String start_date;
	private String end_date;
	
	public Accumulator(){
		this.msisdn = null;
		this.acc_id = null;
		this.acc_value_abs = null;
		this.acc_value_rel = null;
		this.start_date = null;
		this.end_date = null;
	}
	
	public Accumulator(String msisdn){
		this.msisdn = msisdn;
		this.acc_id = null;
		this.acc_value_abs = null;
		this.acc_value_rel = null;
		this.start_date = null;
		this.end_date = null;
	}
	
	public String getMSISDN(){
		return msisdn;
	}
	
	public String getAccId(){
		return acc_id;
	}
	
	public String getAccValueAbs(){
		return acc_value_abs;
	}
	
	public String getAccValueRel(){
		return acc_value_rel;
	}
	
	public String getStartDate(){
		return start_date;
	}
	
	public String getEndDate(){
		return end_date;
	}
	
	public void setMSISDN(String msisdn){
		this.msisdn=msisdn;
	}
	
	public void setAccId(String acc_id){
		this.acc_id=acc_id;
	}
	
	public void setAccValueAbs(String acc_value_abs){
		this.acc_value_abs=acc_value_abs;
	}
	
	public void setAccValueRel(String acc_value_rel){
		this.acc_value_rel=acc_value_rel;
	}
	
	public void setStartDate(String start_date){
		this.start_date=start_date;
	}
	
	public void setEndDate(String end_date){
		this.end_date=end_date;
	}
}
