/**
 * Contains all resources on SDP and SSH activated on a subscriber. The GET action in "AllResource" page
 * get all resources activated on a subscriber. The list of this resources are stored in this class, for a single
 * user.
 */

package com.innovery.mpm.allresource.implementations.util.bean;

import java.util.ArrayList;
import java.util.List;

import com.innovery.mpm.allresource.interfaces.util.bean.ActivatedSubscriberResourceInterface;

public class ActivatedSubscriberResource implements ActivatedSubscriberResourceInterface{

	private List<String> daList;
	private List<String> accumulatorList;
	private List<String> offerIdList;
	private String community;
	private List<String> fafList;
	private String so;
	private List<String> pplanList;
	private String agi;
	private List<String> UC;
	private List<String> UT;
	private List<String> scList;

	public ActivatedSubscriberResource() {
		daList = new ArrayList<String>();
		accumulatorList = new ArrayList<String>();
		offerIdList = new ArrayList<String>();
		fafList = new ArrayList<String>();
		pplanList = new ArrayList<String>();
		UC = new ArrayList<String>();
		UT = new ArrayList<String>();
		scList = new ArrayList<String>();
	}

	public List<String> getDaList() {
		return daList;
	}

	public void setDaList(List<String> daList) {
		this.daList = daList;
	}

	public void addDa(String da) {
		this.daList.add(da);
	}

	public List<String> getAccumulatorList() {
		return accumulatorList;
	}

	public void setAccumulatorList(List<String> accumulatorList) {
		this.accumulatorList = accumulatorList;
	}

	public void addAccumulator(String acc) {
		this.accumulatorList.add(acc);
	}

	public List<String> getOfferList() {
		return offerIdList;
	}

	public void setOfferList(List<String> offerList) {
		this.offerIdList = offerList;
	}

	public void addOffer(String offer) {
		this.offerIdList.add(offer);
	}

	public String getCommunity() {
		return community;
	}

	public void setCommunity(String community) {
		this.community = community;
	}

	public List<String> getFafList() {
		return fafList;
	}

	public void setFafList(List<String> fafList) {
		this.fafList = fafList;
	}

	public void addFaf(String faf) {
		this.fafList.add(faf);
	}

	public String getSo() {
		return so;
	}

	public void setSo(String so) {
		this.so = so;
	}

	public List<String> getPplanList() {
		return pplanList;
	}

	public void setPplanList(List<String> pplanList) {
		this.pplanList = pplanList;
	}

	public void addPromotionPlan(String pplan) {
		this.pplanList.add(pplan);
	}

	public String getAgi() {
		return agi;
	}

	public void setAgi(String agi) {
		this.agi = agi;
	}

	public List<String> getUC() {
		return UC;
	}

	public void setUC(List<String> UC) {
		this.UC = UC;
	}

	public void addUC(String uc) {
		this.UC.add(uc);
	}

	public List<String> getUT() {
		return UT;
	}

	public void setUT(List<String> UT) {
		this.UT = UT;
	}

	public void addUT(String ut) {
		this.UT.add(ut);
	}

	public List<String> getServiceCode() {
		return scList;
	}

	public void setServiceCode(List<String> serviceCode) {
		this.scList = serviceCode;
	}

	public void addServiceCode(String sc) {
		this.scList.add(sc);
	}
}
